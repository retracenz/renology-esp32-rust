//! Minimal 8x8 font covering just the glyphs needed for the banner text.
//! Each glyph is 8 rows, MSB = leftmost pixel.

pub const GLYPH_WIDTH: i32 = 8;
pub const GLYPH_HEIGHT: i32 = 8;

pub fn glyph(c: char) -> [u8; 8] {
    match c {
        'g' => [
            0b00000000,
            0b00000000,
            0b00111100,
            0b01000010,
            0b01000010,
            0b00111110,
            0b00000010,
            0b00111100,
        ],
        'o' => [
            0b00000000,
            0b00000000,
            0b00111100,
            0b01000010,
            0b01000010,
            0b01000010,
            0b00111100,
            0b00000000,
        ],
        'b' => [
            0b01000000,
            0b01000000,
            0b01111100,
            0b01000010,
            0b01000010,
            0b01000010,
            0b01111100,
            0b00000000,
        ],
        'l' => [
            0b00110000,
            0b00010000,
            0b00010000,
            0b00010000,
            0b00010000,
            0b00010000,
            0b00111000,
            0b00000000,
        ],
        'e' => [
            0b00000000,
            0b00000000,
            0b00111100,
            0b01000010,
            0b01111110,
            0b01000000,
            0b00111100,
            0b00000000,
        ],
        _ => [0; 8],
    }
}
